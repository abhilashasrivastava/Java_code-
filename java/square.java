import java.util.*;
public class square {
    /**
     * @param args
     */
    public static void main(String[] args){
        Scanner sc= new Scanner(System.in);
        int n;
        n= sc.nextInt();
        int i;
        
        for(i=1;i<=n;i++)
            System.out.println(i*i);
        
    }
    
}
